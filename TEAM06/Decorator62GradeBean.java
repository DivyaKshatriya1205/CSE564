/**
 * A Bean class which representing a student's single grade instance (assesmentName and score) 
 * 
 * @author Akhilesh Krishnan (akrish84)
 * @since 10/25/2020
 *
 */
public class Decorator62GradeBean {

	private String assesmentName;
	private int score;

	public String getAssesmentName() {
		return assesmentName;
	}

	public void setAssesmentName(String assesmentName) {
		this.assesmentName = assesmentName;
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}
}
